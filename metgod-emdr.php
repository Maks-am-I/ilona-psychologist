    <section class="results-emdr container container-not-padding-x">
      <h2>
          Risultati dell'EMDR
      </h2>
      <ul>
        <li>
          <h3>
            1. Elaborazione delle memorie traumatiche:
          </h3>
          <p>
            Le memorie traumatiche perdono la loro carica emotiva negativa 
            e vengono integrate in modo adattivo
          </p>
        </li>
        <li>
          <h3>
            2. Riduzione dei sintomi:
          </h3>
          <p>
            Sintomi come flashback, incubi, ipervigilanza e ansia diminuiscono 
            o scompaiono
          </p>
        </li>
        <li>
          <h3>
            3. Miglioramento dell'autostima:
          </h3>
          <p>
            I pazienti spesso riportano un aumento della fiducia in sé stessi e 
            una visione più positiva di sé
          </p>
        </li>
        <li>
          <h3>
            4. Cambiamenti cognitivi:
          </h3>
          <p>
            Le convinzioni negative legate al trauma (es. "Sono in pericolo", 
            "Non valgo nulla") vengono sostituite con pensieri più funzionali 
            (es. "Ora sono al sicuro", "Merito di essere amato")
          </p>
        </li>
        <li>
          <h3>
            5. Miglioramento delle relazioni:
          </h3>
          <p>
            I pazienti spesso riportano miglioramenti nelle relazioni interpersonali, 
            grazie a una maggiore regolazione emotiva
          </p>
        </li>
        <li>
          <h3>
            6. Effetti a lungo termine:
          </h3>
          <p>
            I benefici dell'EMDR tendono a essere duraturi, poiché il trauma 
            viene elaborato a livello profondo
          </p>
        </li>
      </ul>
    </section>
