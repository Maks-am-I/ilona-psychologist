          </p>
        </li>
        <li>
          <h3>
            Riduzione dei sintomi fisici ed emotivi:
          </h3>
          <p>
            Aiuta a ridurre l'intensità delle emozioni negative, dei flashback e dei sintomi fisici associati al trauma
          </p>
        </li>
      </ul>
