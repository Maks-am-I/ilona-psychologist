    <section class="how-it-works container">
        <h2>
            Come funziona
        </h2>
        <ul>
            <li>
                1. Il primo incontro inizia col presentazione del problema. 
                Si cerca di capire la natura del disagio e si prospettano 
                le modalità dell’eventuale lavoro
            </li>
            <li>
                2. Durante successive 3-4 sedute io mi focalizzo sulla raccolta 
                di informazioni che riguardano il momento attuale e le tappe 
                principali del passato, si parla della famiglia d’origine, di 
                legami affettivi, di qualità e natura del rapporto con le persone 
                importanti. Al termine di questa fase “conoscitiva”, se necessario, 
                si somministrano i test proiettivi. Nella mia pratica utilizzo il 
                TAT e le tavole di Rorschach (comunemente conosciute come “il test 
                delle macchie d’inchiostro") che permetto di indagare sulle 
                inclinazioni “nascoste” e le dinamiche profonde del carattere.
                In questa fase io presto una particolare attenzione anche al 
                “linguaggio del corpo” — spesso le reazioni corporee sono una 
                fonte più attendibile delle risposte verbali. L’interpretazione 
                fedele del linguaggio corporeo fornisce al professionista 
                informazioni molto utili sulle emozioni che vive una persona e dà 
                l’aiuto prezioso per il futuro lavoro
            </li>
            <li>
                3. La fase successiva — e molto importante! — definita 
                “restituzione”. E’ un incontro dove io presento in forma 
                breve le informazioni che ho raccolto durante la fase iniziale, 
                do l’interpretazione dei risultati dei test somministrati, 
                riassumo la mia visione della situazione e propongo le mie 
                riflessioni sul lavoro da intraprendere. Insieme al paziente 
                definiamo gli obiettivi a breve e lungo termine, diamo le scadenze, 
                decidiamo le modalità degli incontri. Il paziente durante l’incontro 
                di restituzione è invitato a partecipare attivamente, fare domande, 
                chiedere spiegazioni ove necessario
            </li>
            <li>
               4. Dalla seduta successiva si inizia il percorso propriamente psicoterapico 
            </li>
            <li>
                5. Durante la fase finale della terapia spesso consiglio di
                 partecipare agli incontri di gruppo dove puoi condividere le 
                 tue emozioni ed esperienze con gli altri e ricevere il sostegno 
                 psicologico dal gruppo
            </li>
        </ul>
